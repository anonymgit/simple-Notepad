    public void setLineNumberUI()
    {
        lines.setBackground(Color.LIGHT_GRAY);
        lines.setEditable(false);
      //  Code to implement line numbers inside the JTextArea
        editorPane.getDocument().addDocumentListener(new DocumentListener() {
        public String getText() {
            int caretPosition = editorPane.getDocument().getLength();
            Element root = editorPane.getDocument().getDefaultRootElement();
            String text = "1" + System.getProperty("line.separator");
               for(int i = 2; i < root.getElementIndex(caretPosition) + 2; i++) {
                  text += i + System.getProperty("line.separator");
               }
            return text;
         }
         @Override
         public void changedUpdate(DocumentEvent de) {
            lines.setText(getText());
         }
         @Override
         public void insertUpdate(DocumentEvent de) {
            lines.setText(getText());
         }
         @Override
         public void removeUpdate(DocumentEvent de) {
            lines.setText(getText());
         }
      });
      jScrollPane1.getViewport().add(editorPane);
      jScrollPane1.setRowHeaderView(lines);
    }
