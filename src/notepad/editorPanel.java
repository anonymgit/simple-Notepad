    public void save(String fileName) throws FileNotFoundException
    {
        try
        {
            
            FileOutputStream output= new FileOutputStream(fileName);
            String data=editorPane.getText();
            byte[] array = data.getBytes();
            output.write(array);
            output.close(); 
        }
        catch (Exception e) {
            
            System.out.println(e);
        }
    }
    public void open(File selectedFile)
    {
        try
        {
            FileInputStream in=new FileInputStream(selectedFile);
            byte buf[]=new byte[in.available()];
            in.read(buf);
            editorPane.setText(new String(buf));
            textLength=editorPane.getText().length();
        }	
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"ERR!!"+e,"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }
